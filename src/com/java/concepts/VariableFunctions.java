package com.java.concepts;

public class VariableFunctions {
    public static void main(String[] args) {

        //variable is also called as identifier
        int age; //variable declaration
        age = 23; //variable initialization
        //here the int variable directly holds the value 19

        //constants or symbolic constants
        final int SOME_NUM = 10;

        String name = "Navya";
        //string holds the address of the string object in memory

        System.out.println(name + "is "+age);
        System.out.print(name+ " is "+age + "\n"); //\n is new line character

    }
}
